    private void btnregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregisterActionPerformed
        String id = txtadmin.getText().toString().trim();
        String username = txtusername.getText().toString().trim();
        String password = txtpswrd.getText().toString().trim();
        String comPassword = txtcnfrm.getText().toString().trim();
        
        if(!password.equals(comPassword)){
            JOptionPane.showMessageDialog(null, "Password Tidak Sesuai");
        }else if(password.equals("") || username.equals("")){
            JOptionPane.showMessageDialog(null, "Username atau Password yang anda masukkan salah");
        }else{
            try{
                Connection c = koneksi.getKoneksi();
                String sql = "INSERT INTO login VALUES (?,?,?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, id);
                p.setString(2, username);
                p.setString(3, password);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Akun Berhasil dibuat");
                
            } catch (java.sql.SQLException ex) {
                Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
                this.dispose();
                login a = new login();
                a.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnregisterActionPerformed

