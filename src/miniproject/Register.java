    private void autonumber(){
        try{
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM login ORDER BY id_admin DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()){
                String NoId = r.getString("id_admin").substring(2);
                String ID = "" +(Integer.parseInt(NoId)+1);
                String Zero = "";
                
                if (ID.length()==1)
                    {Zero = "00";}
                else if(ID.length()==2)
                    {Zero = "0";}
                else if(ID.length()==3)
                    {Zero = "";}
                
                txtadmin.setText("KR" + Zero + ID);
            }else{
                txtadmin.setText("KR001");                
            }
            r.close();
            s.close();
        }catch(Exception e){
            System.out.println("autonumber error");
            
        }
    }

    
